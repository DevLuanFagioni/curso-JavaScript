// Obs: nunca utilizar var, faz parte de uma sintaxe antiga onde na versão ES6 temos formas muito melhores de utilizar variaveis, como: const e let

var nome = 'luan'
var nome = 'ana'
// com var é possivel redeclarar uma variavel mas isso não é um bom uso em js

let nome = 'luan'
nome = 'ana'
//com let podemos redefinir o valor da variavel